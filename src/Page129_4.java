
public class Page129_4 {
    public static void main(String[] args) {

        // Character --> Decimal
        char c = 'A';
        int i = (int)c;
        System.out.println(i);

        // Float --> Int
        float f = 1000.34f;
        int i2 = (int)f;
        System.out.println(i2);

        // Double --> Int
        double d = 1000.34;
        int i3 = (int)d;
        System.out.println(i3);

        // Decimal --> Character
        int i4 = 97;
        char c2 = (char)i4;
        System.out.println(c2);

    }
}
